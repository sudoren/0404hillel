package lesson3;

public class Maths {

    public static double sum(double a1, double b1) {
        return a1+b1;
    }

    public static double diff(double a1, double b1) {
        return a1-b1;
    }

    public static double mult(double a1, double b1) {
        return a1*b1;
    }

    public static double div(double a1, double b1) {
        return a1/b1;
    }

    public static double percent(double a1, double b1) {
        return (a1*b1)/100;
    }
}
